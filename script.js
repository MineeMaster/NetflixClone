function toggle1() {
    var data = document.getElementById("data1");
    var fafaplus = document.getElementById("fa-plus");
    var fafaminus = document.getElementById("fa-minus");
    if (data.style.display === "none") {
        data.style.display = "block";
        fafaminus.style.display = "block";
        fafaplus.style.display = "none";
        // data.innerHTML = "นี่คือข้อมูล";
    } else {
        data.style.display = "none"; 
        fafaminus.style.display = "none";
        fafaplus.style.display = "block";
        // data.innerHTML = "";
    }
}

